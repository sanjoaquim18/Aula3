package exemplo02;

public class Calculadora {

    int total;

    public int somar(int x, int y){
        total = x+y;
        return total;
    }


    public int subtrair(int x, int y){
        total = x-y;
        return total;
    }

    public int multiplic(int x, int y){
        total = x*y;
        return total;
    }

}
